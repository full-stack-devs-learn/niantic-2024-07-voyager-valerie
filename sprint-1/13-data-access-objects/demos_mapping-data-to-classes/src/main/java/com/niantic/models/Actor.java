package com.niantic.models;

public class Actor {

    private int actorId;
    private String firstName;
    private String lastName;

    public Actor()
    {
        this.actorId = actorId;
        this.firstName = firstName;
        this.lastName = lastName;
    }

    public int getActorId()
    {
        return actorId;
    }

    public int setActorId()
    {
        this.actorId = actorId;
    }
}
